/** 위험도 계산 (감소율 기반) */
export function calcRiskLevel(school) {
  const data = school.enrollment
  if (data.length < 2) return 'low'
  const first = data[0].count
  const last = data[data.length - 1].count
  if (first === 0) return 'high'
  const dropRate = (first - last) / first
  if (dropRate >= 0.7 || last <= 5) return 'high'
  if (dropRate >= 0.4 || last <= 20) return 'medium'
  return 'low'
}

